"""
+ - * /  %  ** //
"""
num1 = 100
num2 = 20

sum = num1 + num2
print(sum)

dec = num1 - num2
print(dec)

res = num1 * num2
print(res)

# / 结果为符点数
res = num1 / num2
print(res)
res = 5/3
print(res)

# // 取结果的整数部分
res = num1 // num2
print(res)
res = 5//3
print(res)

# % 取余数
res = num1 % num2
print(res)
res = 5%3
print(res)

# **  幂运算   2的3次方，2**3
print(2**5)

# 多个运算符  优先级  括号括起来
s = ((num1 + 200) / 1000) ** 3
print(s)